package Bean;

/**
 *
 * @author ANISH
 */
public class Open_Account {
    private int Account_No;
    private String date ;
    
    private String name;
    private String Address;
    private String Email;
    private long Contact_No;
    private int Amount;
    private String Account_Type;

    /**
     * @return the Account_No
     */
    public int getAccount_No() {
        return Account_No;
    }

    /**
     * @param Account_No the Account_No to set
     */
    public void setAccount_No(int Account_No) {
        this.Account_No = Account_No;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the Address
     */
    public String getAddress() {
        return Address;
    }

    /**
     * @param Address the Address to set
     */
    public void setAddress(String Address) {
        this.Address = Address;
    }

    /**
     * @return the Email
     */
    public String getEmail() {
        return Email;
    }

    /**
     * @param Email the Email to set
     */
    public void setEmail(String Email) {
        this.Email = Email;
    }

    /**
     * @return the Contact_No
     */
    public long getContact_No() {
        return Contact_No;
    }

    /**
     * @param Contact_No the Contact_No to set
     */
    public void setContact_No(long Contact_No) {
        this.Contact_No = Contact_No;
    }

    /**
     * @return the Amount
     */
    public int getAmount() {
        return Amount;
    }

    /**
     * @param Amount the Amount to set
     */
    public void setAmount(int Amount) {
        this.Amount = Amount;
    }

    /**
     * @return the Account_Type
     */
    public String getAccount_Type() {
        return Account_Type;
    }

    /**
     * @param Account_Type the Account_Type to set
     */
    public void setAccount_Type(String Account_Type) {
        this.Account_Type = Account_Type;
    }
    

   

   
}
